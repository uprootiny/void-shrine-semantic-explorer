;; Dynamic ontology selection based on entropy source characteristics
(defn select-ontological-mode [source-name source-data]
  "Intelligently select ontological visualization mode based on source characteristics"
  (let [quality (:quality source-data)
        entropy-bits (:entropy-bits-per-sample source-data 20)
        method (:method source-data)
        samples (:samples source-data)]
    (cond
      ;; Quantum sources map to quantum indeterminacy
      (or (= quality "quantum-true") (= quality "quantum-certified"))
      (rand-nth [:quantum-vacuum-fluctuations :superposition-states :measurement-paradoxes])
      
      ;; Nuclear/radioactive sources map to stellar dissolution  
      (or (= quality "nuclear-random") (.contains method "decay"))
      (rand-nth [:nuclear-decay :fusion-failure :isotope-transformation])
      
      ;; Network/timing sources map to temporal voids
      (or (.contains source-name "network") (.contains source-name "timing"))
      (rand-nth [:chronos-dissolution :temporal-eddies :anachronistic-zones])
      
      ;; Memory/system sources map to neural dissolution
      (or (.contains source-name "memory") (.contains source-name "system"))
      (rand-nth [:neural-dissolution :synaptic-degradation :memory-dissolution])
      
      ;; Atmospheric sources map to entropic cascade
      (.contains source-name "atmospheric")
      (rand-nth [:dissolution-waves :chaos-emergence :butterfly-triggers])
      
      ;; High entropy sources map to linguistic void
      (> entropy-bits 22)
      (rand-nth [:semantic-collapse :communication-static :babel-confusion])
      
      ;; Default to primordial absence variations
      :else
      (rand-nth [:primordial-absence :pre-cosmic-silence :foundational-emptiness]))))